        // program showing the concept of relational operator
        
        #include<stdio.h>
        void main()
        {
                int a=5,b=2,c;
                
                c = a<b;
                printf("%d < %d = %d\n",a,b,c);
                
                c = a>b;
                printf("%d > %d = %d\n",a,b,c);        
        
                c = a<=b;
                printf("%d <= %d = %d\n",a,b,c);
        
                c = a>=b;
                printf("%d >= %d = %d\n",a,b,c);
        
                c = a==b;
                printf("%d == %d = %d\n",a,b,c);
        
                c = a!=b;
                printf("%d != %d = %d\n",a,b,c);
        
        }
        
        
        
        
        
        
