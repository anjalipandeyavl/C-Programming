        // program to print Hello world
        
        #include<stdio.h>
        void main()
        {
                int a = 100;
                float b = 234.45;
                char ch = 'a';
                
                printf("value of a : %d\n",a);
                printf("value of b : %f\n",b);
                printf("value of b : %.1f\n",b);
                printf("value of b : %.2f\n",b);
                printf("value of b : %.8f\n",b);
                printf("value of ch : %c\n",ch);        
        }
        /*
        Format specifier
                %d = integer
                %c = character
                %f = float
                %ld = long
                %lf = double
                %Lf = long double
                %i = short
                %u = unsigned integer
                %lu = long unsigned integer
        */
        
        
        
        
        
        
