        // program showing the concept of arithmetic operator
        
        #include<stdio.h>
        void main()
        {
                int a=5,b=2,c,d;
                
                c = a+b;
                d = a-b;
                
                printf("%d + %d = %d\n",a,b,c);
                printf("%d - %d = %d\n",a,b,d);        
        
                c = a*b;
                printf("%d * %d = %d\n",a,b,c);
        
                c = a/b;
                printf("%d / %d = %d\n",a,b,c);
        
                c = a%b;
                printf("%d %% %d = %d\n",a,b,c);
        }
        
        
        
        
        
        
