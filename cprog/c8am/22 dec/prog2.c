	// program showing the concept of switch control statement
	 	// output : error duplicate case value
	 #include<stdio.h>
	 void main()
	 {
	 	char ch='a';
	 		switch(ch)
	 		{
	 			case 'a' : printf("Character\n");
	 								break;
	 			case 97 : printf("ASCII Value\n");
	 								break;
	 			default : printf("Invalid selection\n");
	 								break;
	 		}
	 }	

