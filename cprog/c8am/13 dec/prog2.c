
	// program showing the concept of escape sequence character
	#include<stdio.h>
	void main()
	{
		printf("Hello world\n");
		printf("\tHello world\n");
		printf("Hello \tworld\n");
		printf("Hello \"world\"\n");
		printf("Hello \'world\'\n");
	  printf("Hello 'world'\n");	
	  printf("Hello\bworld\n");						
		printf("Hello\vworld");
	}
