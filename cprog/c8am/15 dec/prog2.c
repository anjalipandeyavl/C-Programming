	// program showing the concept of increment | decrement  operator
	#include<stdio.h>
	void main()
	{
		int a=10,b=10,c=10,d=10;
		
		printf("a++ : %d\n",a++);
		printf("a : %d\n\n",a);
		
		printf("++b : %d\n",++b);
		printf("b : %d\n\n",b);
		
		printf("c-- : %d\n",c--);
		printf("c : %d\n\n",c);
		
		printf("--d : %d\n",--d);
		printf("d : %d\n\n",d);
	}
